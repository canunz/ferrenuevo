import React from 'react';
import { motion } from 'framer-motion';
import { 
  ShoppingCartIcon, 
  HeartIcon,
  StarIcon,
  EyeIcon
} from '@heroicons/react/24/outline';
import { HeartIcon as HeartSolid, StarIcon as StarSolid } from '@heroicons/react/24/solid';
import { Link } from 'react-router-dom';

const TarjetaProducto = ({ 
  producto, 
  onAgregarAlCarrito, 
  onToggleFavorito, 
  esFavorito = false,
  mostrarAcciones = true,
  moneda = 'CLP',
  tiposCambio = { USD: 1, EUR: 1 }
}) => {
  const convertirPrecio = (precioCLP) => {
    if (moneda === 'CLP') return new Intl.NumberFormat('es-CL', { style: 'currency', currency: 'CLP' }).format(precioCLP);
    if (moneda === 'USD') {
      const v = tiposCambio.USD;
      if (!v) return 'N/D';
      return `US$ ${(precioCLP / v).toLocaleString('en-US', { maximumFractionDigits: 2 })}`;
    }
    if (moneda === 'EUR') {
      const v = tiposCambio.EUR;
      if (!v) return 'N/D';
      return `€ ${(precioCLP / v).toLocaleString('de-DE', { maximumFractionDigits: 2 })}`;
    }
    return precioCLP;
  };

  const obtenerImagenProducto = (imagen) => {
    if (!imagen) {
      return '/assets/imagenes/productos/placeholder.jpg';
    }
    // CORRECCIÓN DEFINITIVA DE RUTA DE IMAGEN
    return `http://localhost:3000/static/productos/${imagen}`;
  };

  // DEMOSTRACIÓN: Aplicar un descuento visual a un producto específico
  if (producto.nombre.toLowerCase().includes('sierra circular bosch')) {
    producto.precio_oferta = producto.precio * 0.90; // 10% de descuento
  }

  const tieneDescuento = producto.precio_oferta && producto.precio_oferta < producto.precio;

  // Calcular el stock total sumando todos los inventarios
  let stockTotal = 0;
  if (Array.isArray(producto.inventario) && producto.inventario.length > 0) {
    stockTotal = producto.inventario.reduce((sum, inv) => sum + (inv.stock_actual || 0), 0);
  } else if (typeof producto.stock_actual === 'number') {
    stockTotal = producto.stock_actual;
  }

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.3 }}
      className="bg-white rounded-lg shadow-md hover:shadow-lg transition-all duration-300 overflow-hidden group"
    >
      {/* Imagen del producto */}
      <div className="relative aspect-square overflow-hidden bg-gray-100">
        <img
          src={obtenerImagenProducto(producto.imagen)}
          alt={producto.nombre}
          className="w-full h-full object-cover group-hover:scale-105 transition-transform duration-300"
          onError={(e) => {
            // Si la imagen falla, mostramos un ícono de producto
            e.target.style.display = 'none';
            e.target.nextSibling.style.display = 'flex';
          }}
        />
        
        {/* Fallback cuando la imagen no carga */}
        <div 
          className="hidden w-full h-full items-center justify-center bg-gray-200"
          style={{ display: 'none' }}
        >
          <div className="text-center">
            <svg className="mx-auto h-16 w-16 text-gray-400" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M20 13V6a2 2 0 00-2-2H6a2 2 0 00-2 2v7m16 0v5a2 2 0 01-2 2H6a2 2 0 01-2-2v-5m16 0h-2.586a1 1 0 00-.707.293l-2.414 2.414a1 1 0 01-.707.293h-3.172a1 1 0 01-.707-.293l-2.414-2.414A1 1 0 006.586 13H4" />
            </svg>
            <p className="mt-2 text-sm text-gray-500">Sin imagen</p>
          </div>
        </div>
        
        {/* Badge de descuento */}
        {tieneDescuento && (
          <div className="absolute top-2 left-2 bg-red-500 text-white text-xs font-bold px-2 py-1 rounded">
            -{Math.round(((producto.precio - producto.precio_oferta) / producto.precio) * 100)}%
          </div>
        )}

        {/* Badge de marca */}
        {producto.marca && (
          <div className="absolute top-2 right-2 bg-blue-500 text-white text-xs font-bold px-2 py-1 rounded">
            {producto.marca.nombre}
          </div>
        )}

        {/* Acciones rápidas */}
        {mostrarAcciones && (
          <div className="absolute inset-0 bg-black bg-opacity-0 group-hover:bg-opacity-20 transition-all duration-300 flex items-center justify-center">
            <div className="flex space-x-2 opacity-0 group-hover:opacity-100 transition-opacity duration-300">
              <button
                onClick={() => onToggleFavorito(producto.id)}
                className="bg-white p-2 rounded-full shadow-lg hover:bg-red-50 transition-colors"
              >
                {esFavorito ? (
                  <HeartSolid className="w-5 h-5 text-red-500" />
                ) : (
                  <HeartIcon className="w-5 h-5 text-gray-600" />
                )}
              </button>
              
              <Link
                to={`/producto/${producto.id}`}
                className="bg-white p-2 rounded-full shadow-lg hover:bg-blue-50 transition-colors"
              >
                <EyeIcon className="w-5 h-5 text-gray-600" />
              </Link>
            </div>
          </div>
        )}
      </div>

      {/* Información del producto */}
      <div className="p-4">
        {/* Categoría */}
        {producto.categoria && (
          <div className="text-xs text-gray-500 mb-1">
            {producto.categoria.nombre}
          </div>
        )}

        {/* Nombre del producto */}
        <h3 className="font-semibold text-gray-900 mb-2 line-clamp-2 group-hover:text-blue-600 transition-colors">
          {producto.nombre}
        </h3>

        {/* CÓDIGO SKU */}
        {producto.codigo_sku && (
          <p className="text-xs text-gray-400 mb-2 font-mono">
            SKU: {producto.codigo_sku}
          </p>
        )}

        {/* Descripción */}
        {producto.descripcion && (
          <p className="text-sm text-gray-600 mb-3 line-clamp-2">
            {producto.descripcion}
          </p>
        )}

        {/* Precios */}
        <div className="mb-3">
          {tieneDescuento ? (
            <div className="flex items-center space-x-2">
              <span className="text-lg font-bold text-red-600">
                {convertirPrecio(producto.precio_oferta)}
              </span>
              <span className="text-sm text-gray-500 line-through">
                {convertirPrecio(producto.precio)}
              </span>
            </div>
          ) : (
            <span className="text-lg font-bold text-gray-900">
              {convertirPrecio(producto.precio)}
            </span>
          )}
        </div>

        {/* Stock */}
        <div className="text-sm text-gray-600 mb-3">
          {stockTotal > 0 ? (
            <span className="text-green-600 font-semibold">✓ Stock: {stockTotal}</span>
          ) : (
            <span className="text-red-600 font-semibold">✗ Agotado</span>
          )}
        </div>

        {/* Botones de acción */}
        <div className="space-y-2">
          <button
            onClick={() => stockTotal > 0 && onAgregarAlCarrito(producto)}
            className={`w-full py-2 px-4 rounded-lg flex items-center justify-center space-x-2 transition-colors font-semibold ${stockTotal > 0 ? 'bg-blue-600 hover:bg-blue-700 text-white cursor-pointer' : 'bg-gray-300 text-gray-400 cursor-not-allowed'}`}
            disabled={stockTotal === 0}
          >
            <ShoppingCartIcon className="w-5 h-5" />
            <span>Agregar al carrito</span>
          </button>
          {/* BOTÓN "VER DETALLE" SIEMPRE VISIBLE */}
          <Link
            to={`/producto/${producto.id}`}
            className="w-full block text-center bg-yellow-400 hover:bg-yellow-500 text-gray-900 font-semibold py-2 px-4 rounded-lg transition-colors border border-yellow-500 shadow"
            style={{ marginTop: '0.5rem' }}
          >
            Ver Detalle
          </Link>
        </div>
      </div>
    </motion.div>
  );
};

export default TarjetaProducto;
